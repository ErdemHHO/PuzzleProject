import mongoose from 'mongoose';

const userShema = mongoose.model('user',{
    email:{
        type: String,
        required: true
    },
    password:{
        type: String,
        required: true
    },
    name:{
        type: String,
        required: true
    },
    surname:{
        type: String,
        required: true
    },
});

export default userShema;